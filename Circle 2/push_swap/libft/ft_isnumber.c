int	ft_isnumber(const char *str)
{
	int	i;

	i = 0;
	if (str[0] == '-')
	{
		if (str[1] == '\0')
			return (-1);
		i++;
	}
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			return (-1);
		i++;
	}
	return (1);
}
